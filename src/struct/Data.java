package struct;

import java.io.Serializable;

public class Data implements Serializable {
	private static final long serialVersionUID = -4460660903766402949L;
	private int ocurr_number;
	private Article article;
	
	public Data(int ocurr_number, Article art) {
		this.ocurr_number = ocurr_number;
		this.article = art;
	}

	public int getOcurr_number() {
		return ocurr_number;
	}

	public void setOcurr_number(int ocurr_number) {
		this.ocurr_number = ocurr_number;
	}

	public Article getArticle() {
		return article;
	}

}
